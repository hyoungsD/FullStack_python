### 변수(variable)

# •변수는 값을 저장하기 위해 사용
# •프로그램에서 필요한 데이터를 임시적으로 저장할 수 있는 공간
# 에 이름(식별자) 붙인것
# •변수명을 통해 자유롭게 데이터에 대해 읽기, 쓰기, 덮어쓰기 가능
# •변수의 선언없이 바로 사용가능
# •age = 27 라고 입력하면 age란 변수가 만들어지고 27을 저장
# •name = ‘홍길동’


## 변수 작명규칙
# •영문자(alphabet), 숫자, 언더스코어(“_”) 만 사용가능
# •중간에 공백이 들어갈 수 없음
# •python 키워드는 사용할 수 없음
# •숫자는 제일 앞에 올 수 없음
# •영문자 혹은 언더스코어(“_”) 만이 첫글자로 올 수 있음
# •대소문자 구분, 변수의 시작은 소문자를 사용
# •가능한 변수명에 역할에 맞은 의미를 부여할것

